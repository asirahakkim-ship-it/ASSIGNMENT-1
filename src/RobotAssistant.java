public class RobotAssistant {
    public static void main(String[] args) {
        int a=10 ,b=5;
        int op=2;
        if(op==1){
            System.out.println(a+b);
        }
        else if(op==4){
            System.out.println(a/b);
        }
        else if(op==2){
            System.out.println(a-b);
        }
        else{
            System.out.println(a*b);
        }
    }
}
